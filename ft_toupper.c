#include "libft.h"

int	ft_toupper(int c)
{
	if ((c >= 65 && c <= 90) || (c >= 97 && c <= 122))
	{
		if (c >= 97 && c <= 122)
			c = c - 32;
		return (c);
	}
	return (c);
}

